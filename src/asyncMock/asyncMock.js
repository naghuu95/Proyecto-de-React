//Funcion getProductos retorna una promesa que es resuelta en 2000 milisegundos

export const getProductos =()=>{
    return new Promise((resolve)=>{
        setTimeout(()=>{
            resolve(productos)
        },2000)
    })
}


export const getProductoById=(itemId)=>{
    return new Promise((resolve)=>{
        setTimeout(()=>{
           resolve(productos.find(prod=>prod.id=== parseInt(itemId)))
        },2000)
    })
}

export const getProductosByCategoria=(productoCategoria)=>{
    return new Promise((resolve)=>{
        setTimeout(()=>{
           resolve(productos.filter(prod=>prod.categoria=== productoCategoria))
        },2000)
    })
}